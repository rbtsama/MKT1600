<script>
export default {
  name: 'AssetTable',
  props: {
    /**
     * Ë°®Ê†ºÊ†áÈ¢ò
     */
    title: {
      type: String,
      required: true
    },
    /**
     * Ë°®Ê†ºÂàóÂÆö‰πâ
     */
    columns: {
      type: Array,
      required: true
    },
    /**
     * Ë°®Ê†ºÊï∞ÊçÆ
     */
    data: {
      type: Array,
      required: true
    }
  },
  data() {
    return {
      searchQuery: '',
      sortKey: 'lastUsed',
      sortOrder: 'asc',
      currentPage: 1,
      itemsPerPage: 20,
      activeFilters: {},
      filters: {
        lastUsed: '-1',
        lastInactive: '-1',
        lastPenalty: '-1',
        usageCount: '-1',
        deletionCount: '-1',
        banCount: '-1'
      }
    }
  },
  computed: {
    /**
     * ËøáÊª§ÂêéÁöÑÊï∞ÊçÆ
     */
    filteredData() {
      return this.data.filter(item => {
        // ÊêúÁ¥¢ËøáÊª§
        let passSearch = true;
        if (this.searchQuery !== '') {
          passSearch = Object.values(item).some(value => {
            if (value === null) return false;
            return String(value).toLowerCase().includes(this.searchQuery.toLowerCase());
          });
        }
        
        // Êù°‰ª∂ËøáÊª§
        let passFilters = true;
        
        // ÊúÄËøë‰ΩøÁî®Á≠õÈÄâ
        if (this.activeFilters.lastUsed !== undefined && parseInt(this.activeFilters.lastUsed) > 0) {
          passFilters = passFilters && item.lastUsed >= parseInt(this.activeFilters.lastUsed);
        }
        
        // ÊúÄËøëÂÅúÁî®Á≠õÈÄâ
        if (this.activeFilters.lastInactive !== undefined && parseInt(this.activeFilters.lastInactive) > 0) {
          passFilters = passFilters && (item.lastInactive !== null && item.lastInactive >= parseInt(this.activeFilters.lastInactive));
        }
        
        // ÊúÄËøëÂ§ÑÁΩöÁ≠õÈÄâ
        if (this.activeFilters.lastPenalty !== undefined && parseInt(this.activeFilters.lastPenalty) > 0) {
          passFilters = passFilters && (item.lastPenalty !== null && item.lastPenalty >= parseInt(this.activeFilters.lastPenalty));
        }
        
        // ‰ΩøÁî®Ê¨°Êï∞Á≠õÈÄâ
        if (this.activeFilters.usageCount !== undefined && parseInt(this.activeFilters.usageCount) >= 0) {
          passFilters = passFilters && item.usageCount < parseInt(this.activeFilters.usageCount);
        }
        
        // Âà†Â∏ñÂ§ÑÁΩöÁ≠õÈÄâ
        if (this.activeFilters.deletionCount !== undefined && parseInt(this.activeFilters.deletionCount) >= 0) {
          passFilters = passFilters && item.deletionCount < parseInt(this.activeFilters.deletionCount);
        }
        
        // Â∞ÅÂè∑Â§ÑÁΩöÁ≠õÈÄâ
        if (this.activeFilters.banCount !== undefined && parseInt(this.activeFilters.banCount) >= 0) {
          passFilters = passFilters && item.banCount < parseInt(this.activeFilters.banCount);
        }
        
        return passSearch && passFilters;
      }).sort((a, b) => {
        let aValue = a[this.sortKey];
        let bValue = b[this.sortKey];
        
        // Â§ÑÁêÜnullÂÄºÂíå0ÂÄºÔºàÂØπ‰∫élastUsedÂ≠óÊÆµÔºå0Ë°®Á§∫"0Â§©Ââç"ÔºåÂ∫îËØ•ÊéíÂú®ÊúÄÂâçÈù¢Ôºâ
        if (this.sortKey === 'lastUsed') {
          // Â§ÑÁêÜnullÊàñundefinedÂÄºÔºåÊéíÂú®ÊúÄÂêé
          if (aValue === null || aValue === undefined) return this.sortOrder === 'asc' ? 1 : -1;
          if (bValue === null || bValue === undefined) return this.sortOrder === 'asc' ? -1 : 1;
          
          // ‰ΩøÁî®Êï∞ÂÄºÊØîËæÉ
          return this.sortOrder === 'asc' ? aValue - bValue : bValue - aValue;
        }
        
        // Â§ÑÁêÜÂÖ∂‰ªñÂ≠óÊÆµÁöÑnullÂÄº
        if (aValue === null || aValue === undefined) return this.sortOrder === 'asc' ? 1 : -1;
        if (bValue === null || bValue === undefined) return this.sortOrder === 'asc' ? -1 : 1;
        
        // Â§ÑÁêÜÂ≠óÁ¨¶‰∏≤ÊØîËæÉ
        if (typeof aValue === 'string' && typeof bValue === 'string') {
          return this.sortOrder === 'asc' 
            ? aValue.localeCompare(bValue)
            : bValue.localeCompare(aValue);
        }
        
        // Â§ÑÁêÜÊï∞ÂÄºÊØîËæÉ
        return this.sortOrder === 'asc' ? aValue - bValue : bValue - aValue;
      });
    },
    
    /**
     * ÂàÜÈ°µÂêéÁöÑÊï∞ÊçÆ
     */
    paginatedData() {
      const start = (this.currentPage - 1) * this.itemsPerPage;
      const end = start + this.itemsPerPage;
      return this.filteredData.slice(start, end);
    },
    
    /**
     * ÊÄªÈ°µÊï∞
     */
    totalPages() {
      return Math.ceil(this.filteredData.length / this.itemsPerPage) || 1;
    },
    
    /**
     * ÂΩìÂâçÈ°µËµ∑ÂßãÈ°π
     */
    startItem() {
      return (this.currentPage - 1) * this.itemsPerPage + 1;
    },
    
    /**
     * ÂΩìÂâçÈ°µÁªìÊùüÈ°π
     */
    endItem() {
      return Math.min(this.currentPage * this.itemsPerPage, this.filteredData.length);
    }
  },
  methods: {
    /**
     * Â∫îÁî®Á≠õÈÄâÊù°‰ª∂
     */
    applyFilters() {
      this.activeFilters = JSON.parse(JSON.stringify(this.filters));
      this.currentPage = 1; // ÈáçÁΩÆÂà∞Á¨¨‰∏ÄÈ°µ
    },
    
    /**
     * ÈáçÁΩÆÁ≠õÈÄâÊù°‰ª∂
     */
    resetFilters() {
      this.filters = {
        lastUsed: '-1',
        lastInactive: '-1',
        lastPenalty: '-1',
        usageCount: '-1',
        deletionCount: '-1',
        banCount: '-1'
      };
      this.activeFilters = JSON.parse(JSON.stringify(this.filters));
      this.currentPage = 1;
    },
    
    /**
     * ËÆæÁΩÆÊéíÂ∫èÊñπÂºè
     */
    sortBy(key) {
      if (this.sortKey === key) {
        this.sortOrder = this.sortOrder === 'asc' ? 'desc' : 'asc';
      } else {
        this.sortKey = key;
        this.sortOrder = 'desc';
      }
    },
    
    /**
     * Êü•ÁúãËµÑ‰∫ßËØ¶ÊÉÖ
     */
    viewDetails(item) {
      this.$emit('view-details', item);
    },
    
    /**
     * Ëé∑ÂèñÁä∂ÊÄÅCSSÁ±ª
     */
    getStatusClass(status) {
      if (status === 'available') return 'inline-flex items-center px-2.5 py-0.5 rounded-full text-xs font-medium bg-blue-100 text-blue-800';
      if (status === 'in_use') return 'inline-flex items-center px-2.5 py-0.5 rounded-full text-xs font-medium bg-green-100 text-green-800';
      if (status === 'disabled') return 'inline-flex items-center px-2.5 py-0.5 rounded-full text-xs font-medium bg-red-100 text-red-800';
      if (status === 'sold') return 'inline-flex items-center px-2.5 py-0.5 rounded-full text-xs font-medium bg-red-100 text-red-800';
      return 'inline-flex items-center px-2.5 py-0.5 rounded-full text-xs font-medium bg-gray-100 text-gray-800';
    },
    
    /**
     * Ëé∑ÂèñÁä∂ÊÄÅÊòæÁ§∫ÊñáÊú¨
     */
    getStatusText(status) {
      const statusMap = {
        'available': 'ÂèØÁî®',
        'in_use': '‰ΩøÁî®‰∏≠',
        'disabled': 'Á¶ÅÁî®',
        'sold': 'Â∑≤ÂîÆ'
      };
      return statusMap[status] || status;
    },
    
    /**
     * Ëé∑ÂèñÊï¥Â§áÁä∂ÊÄÅÊòæÁ§∫ÊñáÊú¨
     */
    getPrepStatusText(status) {
      const statusMap = {
        'Ready_For_Sale': 'ÂèØÂîÆ',
        'Retail_Photo': 'ÊãçÁÖß‰∏≠',
        'Inspection': 'Ê£ÄÈ™å‰∏≠',
        'SOLD': 'Â∑≤ÂîÆÂá∫'
      };
      return statusMap[status] || status;
    },
    
    /**
     * Ëé∑ÂèñÁ≠âÁ∫ßCSSÁ±ª
     */
    getLevelClass(level) {
      if (level === 'bronze') return 'text-amber-700 font-medium flex items-center';
      if (level === 'silver') return 'text-gray-600 font-medium flex items-center';
      if (level === 'gold') return 'text-yellow-600 font-medium flex items-center';
      return 'text-gray-900 font-medium flex items-center';
    },
    
    /**
     * Ëé∑ÂèñÁ≠âÁ∫ßÊòæÁ§∫ÊñáÊú¨
     */
    getLevelText(level) {
      const levelMap = {
        'bronze': 'ÈùíÈìú',
        'silver': 'ÁôΩÈì∂',
        'gold': 'ÈªÑÈáë'
      };
      return levelMap[level] || level;
    }
  }
}
